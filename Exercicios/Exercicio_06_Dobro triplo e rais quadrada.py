''''Crie um algoritimo que leia um numero e mostre seu dobro, triplo e raiz quadrada'''''

n1 = int(input('Digite um numero inteiro: '))
dobro = n1 * 2
triplo = n1 * 3
raiz = n1 ** 2
print('O numero digitado foi {}\nSeu Dobro é {}\nSeu triplo é {}\nRaiz quadrada é {}'.format(n1,dobro,triplo,raiz))


